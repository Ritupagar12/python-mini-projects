#Demonstrates all comparison operators

a = 10
b = 20

print("a = ", a, "b = ", b)
print("Equal(==): ", a == b)
print("Not Equal(!=): ", a != b)
print("Greater Than (>): ", a > b)
print("Less Than (<): ", a < b)
print("Greater Than or Equal(>=): ", a >= b)
print("Less Than or Equal (<=): ", a <= b)
